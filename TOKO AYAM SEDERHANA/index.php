    <?php
    $jenisMakanan =@$_POST['Menu-pilihan'];
    $jumlahPesanan =@$_POST['pesan'];
    $totalHarga =@$_POST['jmlh'];
    $diskon = 0;

    switch ($jenisMakanan) {
      case 'ayam-geprek':
        if ($jumlahPesanan >= 5) {
          $diskon = 10/100; 
          }
          break;
          case 'ayam-goreng':
              if ($jumlahPesanan >= 7) {
                $diskon = 15/100; 
                } else {
                  $diskon = 5/100; 
                  }
                  break;
                  case 'ayam-goreng-mentega':
                    if ($jumlahPesanan >= 4) {
                      $diskon = 10/100; 
                      }
                      break;
                      case 'ayam-panggang':
                        if ($jumlahPesanan >= 5) {
                          $diskon = 10/100;
                          } 
                          break;
                          case 'ayam-bakar':
                            if ($jumlahPesanan >= 8) {
                              $diskon = 15/100; // 25% diskon jika pesan lebih dari atau sama dengan 10 sate
                              } else {
                                $diskon = 5/100; // 10% diskon jika pesan kurang dari 10 sate
                                }
                                break;
                                default:
                                $diskon = 0; // Tidak ada diskon untuk jenis makanan lainnya
                                break;
                                }
                
                // Hitung total pembayaran setelah diskon
                $totalPembayaran = $totalHarga - ($totalHarga * $diskon);
                // Output hasil
                echo "Jenis Makanan: " . $jenisMakanan . "<br>";
                echo "Jumlah Pesanan: " . $jumlahPesanan . "<br>";
                echo "Total Harga: Rp " . number_format($totalHarga, 2, ",", ".") . "<br>";
                if ($diskon > 0) {
                    echo "Diskon: " . ($diskon * 100) . "%<br>";
                }
                echo "Total Pembelian Adalah: Rp " . number_format($totalPembayaran, 2, ",", ".");
